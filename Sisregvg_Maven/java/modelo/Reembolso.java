package modelo;

import java.util.Date;

public class Reembolso {
    
    private int id;
    private String nomper;
    private String dni;
    private String motivo;
    private String nliquidacion;
    private String forma;
    private Date fecha;
    private String  area;
    private String centrodecosto;
    private String nahorroscorriente;
    private String ncuenta;
    private String estado;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNomper() {
        return nomper;
    }

    public void setNomper(String nomper) {
        this.nomper = nomper;
    }

    public String getDni() {
        return dni;
    }

    public void setDni(String dni) {
        this.dni = dni;
    }

    public String getMotivo() {
        return motivo;
    }

    public void setMotivo(String motivo) {
        this.motivo = motivo;
    }

    public String getNliquidacion() {
        return nliquidacion;
    }

    public void setNliquidacion(String nliquidacion) {
        this.nliquidacion = nliquidacion;
    }

    public String getForma() {
        return forma;
    }

    public void setForma(String forma) {
        this.forma = forma;
    }

    public Date getFecha() {
        return fecha;
    }

    public void setFecha(Date fecha) {
        this.fecha = fecha;
    }

    public String getArea() {
        return area;
    }

    public void setArea(String area) {
        this.area = area;
    }

    public String getCentrodecosto() {
        return centrodecosto;
    }

    public void setCentrodecosto(String centrodecosto) {
        this.centrodecosto = centrodecosto;
    }

    public String getNahorroscorriente() {
        return nahorroscorriente;
    }

    public void setNahorroscorriente(String nahorroscorriente) {
        this.nahorroscorriente = nahorroscorriente;
    }

    public String getNcuenta() {
        return ncuenta;
    }

    public void setNcuenta(String ncuenta) {
        this.ncuenta = ncuenta;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

  

   

}